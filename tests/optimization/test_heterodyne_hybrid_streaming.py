"""Tests for heterodyne stratified hybrid-streaming pipeline (Phase 2)."""
from __future__ import annotations

import numpy as np


def _make_synthetic_heterodyne(n_phi: int = 2, n_t: int = 8, seed: int = 0):
    """Build a tiny synthetic heterodyne dataset from the live kernel.

    Returns (model, c2, phi) where c2 has shape (n_phi, n_t, n_t) and
    is generated by the real compute_c2_heterodyne kernel at the model's
    initial parameters, so residual should be ~0 at those parameters.
    """
    import jax.numpy as jnp

    from xpcsjax.config import ConfigManager
    from xpcsjax.core.heterodyne_jax_backend import compute_c2_heterodyne
    from xpcsjax.core.heterodyne_model_stateful import HeterodyneModel

    cfg = ConfigManager("xpcsjax/config/templates/xpcsjax_two_component.yaml")
    model = HeterodyneModel.from_config(cfg.config)

    t = np.arange(1, n_t + 1, dtype=np.float64) * model.dt
    model.sync_time_axis(t)

    phi = np.linspace(-5.0, 5.0, n_phi).astype(np.float64)
    full = jnp.asarray(model.param_manager.get_full_values(), dtype=jnp.float64)
    contrast, offset = model.scaling.get_for_angle(0)

    c2 = np.stack(
        [
            np.asarray(
                compute_c2_heterodyne(full, model.t, model.q, model.dt, float(p), contrast, offset)
            )
            for p in phi
        ]
    )  # shape: (n_phi, n_t, n_t)

    return model, c2, phi

